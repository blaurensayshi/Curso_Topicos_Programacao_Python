"""
Programa que diz a categoria de um produto de acordo com seu preço
"""

a = input("Digite o preço do produto:")

if a == '10':
    print("Categoria 1")
elif a == '15':
    print("Categoria 2")
elif a == '25':
    print("Categoria 3")
elif a == '30':
    print("Categoria 4")
elif a == '40':
    print("Categoria 5")